<?php
// Файл: config.php
// Назначение: Настройки подключения к базе данных PostgreSQL

// Параметры подключения к БД
$host = 'localhost';    // Хост базы данных (обычно localhost)
$port = '5432';     // Порт PostgreSQL (по умолчанию 5432)
$dbname = 'korochki_portal';    // Имя базы данных
$user = 'postgres';     // Имя пользователя БД (по умолчанию postgres)
$password = '1234';     // Пароль пользователя (по умолчанию postgres или тот, что задавали при установке




// Строка подключения
$connection_string = "host=$host port=$port dbname=$dbname user=$user password=$password";

// Устанавливаем соединение с базой данных
$db_conn = pg_connect($connection_string);

// Проверяем, удалось ли подключиться
if (!$db_conn) {
    // Если подключение не удалось, завершаем скрипт с сообщением об ошибке
    die("Ошибка подключения к базе данных: " . pg_last_error());
} else {
    // Можно раскомментировать для отладки, но на реальном сайте лучше убрать
    // echo "Подключение к БД успешно установлено!";
}

// Устанавливаем кодировку для корректной работы с русскими символами
pg_set_client_encoding($db_conn, "UTF8");
?>